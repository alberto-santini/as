#ifndef AS_POINT_H
#define AS_POINT_H

namespace as {
    /** @struct TwoDimPoint
     *  @brief  Simple named pair of floats to represent a 2D-point.
     */
    struct TwoDimPoint {
        /** @brief  The x coordinate.
         */
        float x;

        /** @brief The y coordinate.
         */
        float y;
    };
}

#endif //AS_POINT_H
